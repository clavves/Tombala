    private int[][] createArrayFromTextFields(JTextField[][] textFields) {
        int[][] array = new int[3][9];
        boolean[] numbersFound = new boolean[91];

        int numberCount = 0;
        int blankCount = 0;

        for (int i = 0; i < array.length; i++) {
            for (int j = 0; j < array[i].length; j++) {
                String input = textFields[i][j].getText().trim();
                if (input.isEmpty() || input.equals("-1")) {
                    array[i][j] = -1;
                    blankCount++;
                } else {
                    try {
                        int num = Integer.parseInt(input);
                        if (num >= 1 && num <= 90 && !numbersFound[num]) {
                            array[i][j] = num;
                            numbersFound[num] = true;
                            numberCount++;
                        } else {
                            return null;
                        }
                    } catch (NumberFormatException e) {
                        return null;
                    }
                }
            }
        }

        for (int i = 0; i < textFields.length; i++) {
            for (int j = 0; j < textFields[i].length; j++) {
                if (textFields[i][j].getText() == null) {
                    array[i][j] = -1;
                    blankCount++;
                }
            }
        }

        if (numberCount != 15 || blankCount != 12) {
            return null;
        }

        return array;
    }

