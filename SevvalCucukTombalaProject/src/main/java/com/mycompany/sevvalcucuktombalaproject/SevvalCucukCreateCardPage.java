    private void createButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createButtonActionPerformed
        int[][] arrayA;
        int[][] arrayB;

        if (playerNumber == 1) {
            arrayA = createArrayFromTextFields(textFieldA);
            if (arrayA == null) {
                JOptionPane.showMessageDialog(null, "Invalid input for Player A. Write 15 different numbers between 1 and 90.");
                return;
            }
            SevvalCucukGamePage g = new SevvalCucukGamePage(1, arrayA);
            g.setVisible(true);
        } else if (playerNumber == 2) {
            arrayA = createArrayFromTextFields(textFieldA);
            if (arrayA == null) {
                JOptionPane.showMessageDialog(null, "Invalid input for Player A. Write 15 different numbers between 1 and 90.");
                return;
            }

            arrayB = createArrayFromTextFields(textFieldB);
            if (arrayB == null) {
                JOptionPane.showMessageDialog(null, "Invalid input for Player B. Write 15 different numbers between 1 and 90.");
                return;
            }
            SevvalCucukGamePage g = new SevvalCucukGamePage(2, arrayA, arrayB);
            g.setVisible(true);
        }
    }//GEN-LAST:event_createButtonActionPerformed

